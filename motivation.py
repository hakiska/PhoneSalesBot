def get_sale_praise(seller_name: str, sale_count: int) -> str:
    """Возвращает похвалу после продажи.

    Args:
        seller_name: имя продавца
        sale_count: порядковый номер продажи за день
    """
    # Проверяем милстоуны
    if sale_count in _MILESTONES:
        return random.choice(_MILESTONES[sale_count]).format(name=seller_name)

    # Обычная похвала — комбинация шаблона + окончания + эмодзи
    emoji = random.choice(_EMOJIS)

    # 50/50 шанс: похвала или мотивация
    if random.random() < 0.5:
        template = random.choice(_PRAISE_TEMPLATES)
        ending = random.choice(_ENDINGS)
        text = template.format(name=seller_name, ending=ending)
    else:
        text = f"{seller_name}, {random.choice(_MOTIVATIONS)}"

    return f"{emoji} {text}"


